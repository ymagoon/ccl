PatientInstructionsComponent.prototype.getDocumentation=function(){var sendAr=["^MINE^",this.getCriterion().person_id+".0",this.getCriterion().encntr_id+".0","^"+this.getConceptCKI()+"^",0];
var loadTimer=new RTMSTimer(this.getComponentLoadTimerName(),this.getCriterion().category_mean);
var self=this;
var getDocumentationRequest=new ScriptRequest().setProgramName("mp_retrieve_blob_by_cki").setParameterArray(sendAr).setLoadTimer(loadTimer).setResponseHandler(function(reply){if(reply.getStatus()==="S"){self.handleSuccess(reply.getResponse());
var data=reply.getResponse();
self.setClinicalEventID(data.CLINICAL_EVENT_ID);
}else{logger.logError("mp_retrieve_blob_by_cki failed: unable to get documentation for "+self.getStyles().getNamespace()+self.getComponentId());
self.finalizeComponent(self.generateScriptFailureHTML(),"");
if(self.m_loadTimer){self.m_loadTimer.fail();
self.m_loadTimer=null;
}}});
getDocumentationRequest.performRequest();
};
PatientInstructionsComponent.prototype.renderComponent=function(recordData){var html=null;
var renderTimer=new RTMSTimer(this.getComponentRenderTimerName(),this.getCriterion().category_mean);
renderTimer.start();
try{var content=recordData&&recordData.BLOB_CONTENTS?recordData.BLOB_CONTENTS:"";
var eventID=recordData&&recordData.EVENT_ID?recordData.EVENT_ID:"0.0";
var eventCD=recordData&&recordData.EVENT_CD?recordData.EVENT_CD:"";
var contentLowerCase=content.toLowerCase();
var indexContentHTML=contentLowerCase.indexOf("<body>");
var indexEndContentHTML=contentLowerCase.indexOf("</body>");
var modContentBodyHTML=content.substring((indexContentHTML+6),indexEndContentHTML);
content=modContentBodyHTML.replace(/[\n\r]/g,"");
html=["<div class='documentation-content'>",content,"</div>"];
this.setDocumentationContent(content);
this.setEventId(eventID);
this.setEventCD(eventCD);
this.finalizeComponent(html.join(""),"");
}catch(err){var errMsg=["<b>",i18n.JS_ERROR,"</b><br><ul><li>",i18n.MESSAGE,": ",err.message,"</li><li>",i18n.NAME,": ",err.name,"</li><li>",i18n.NUMBER,": ",(err.number&65535),"</li><li>",i18n.DESCRIPTION,": ",err.description,"</li></ul>"];
logger.logError(errMsg.join(""));
this.finalizeComponent(this.generateScriptFailureHTML(),"");
if(renderTimer){renderTimer.fail();
renderTimer=null;
}if(this.m_loadTimer){this.m_loadTimer.fail();
this.m_loadTimer=null;
}throw (err);
}finally{if(renderTimer){renderTimer.stop();
renderTimer=null;
}if(this.m_loadTimer){this.m_loadTimer.stop();
this.m_loadTimer=null;
}}};
